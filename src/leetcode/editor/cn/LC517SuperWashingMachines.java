class Solution {
    public int findMinMoves(int[] machines) {
        int sum=0;int N=machines.length;
        for(int ma:machines){
            sum+=ma;
        }
        if(sum%N!=0){
            return -1;
        }
        int  avg=sum/N;
        int res=0;
        int curCount=0;
        for(int num:machines){
            //å½“å‰å·®å€¼
            int ab=num-avg;
            //å½“å‰ç»Ÿè®¡å€¼
            curCount+=ab;
            //reså’ŒcurCountçš„ç»å¯¹å€¼ï¼Œä»¥åŠå½“å‰çš„å·®å€¼æ¯”è¾ƒï¼Œé€‰æœ€å¤§.
            res=Math.max(Math.max(res,Math.abs(curCount)),ab);
        }
        return res;
    }
}
